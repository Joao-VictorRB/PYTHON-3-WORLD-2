n1 = int(input())

print('Digite o segundo número: ',end='')
n2 = int(input())

if n1 > n2:
    print('O primeiro número ({}) é maior'.format(n1))
elif n2 > n1:
    print('O segundo número ({}) é maior'.format(n2))
else:
    print('Não existe número maior, {} e {} são iguais'.format(n1,n2))


